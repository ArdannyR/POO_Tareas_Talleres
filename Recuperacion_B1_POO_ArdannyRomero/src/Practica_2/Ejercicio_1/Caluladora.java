package Practica_2.Ejercicio_1;

public class Caluladora {
    protected int a;
    protected int b;
    protected int c;

    public Caluladora() {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int sumar(int a, int b){
        return a + b;
    }

    public double sumar(double a, double b){
        return a + b;
    }

    public int sumar(int a, int b, int c){
        return a + b + c;
    }
}
